/*Usando ciclos anidados generar las siguientes ternas de numeros */

#include <iostream>

using namespace std;

int main()
{
    int i, j, k, l, m, n, o, p, q, r, s, t, u, v, w, x, y, z, a, b, c, d, e, f, g, h, i2;

    //1 1 1
    for (i = 1; i <= 1; i++) {
        for (j = 1; j <= 1; j++) {
            for (k = 1; k <= 1; k++) {
                cout << i << " " << j << " " << k << endl;
            }
        }
    }
    //2 1 2
    for (l = 2; l <= 2; l++) {
        for (m = 1; m <= 1; m++) {
            for (n = 2; n <= 2; n++) {
                cout << l << " " << m << " " << n << endl;
            }
        }
    }
    //3    1    3
    for (o = 3; o <= 3; o++) {
        for (p = 1; p <= 1; p++) {
            for (q = 3; q <= 3; q++) {
                cout << o << " " << p << " " << q << endl;
            }
        }
    }
    //4    2    1
    for (r = 4; r <= 4; r++) {
        for (s = 2; s <= 2; s++) {
            for (t = 1; t <= 1; t++) {
                cout << r << " " << s << " " << t << endl;
            }
        }
    }
    //5    2    2
    for (u = 5; u <= 5; u++) {
        for (v = 2; v <= 2; v++) {
            for (w = 2; w <= 2; w++) {
                cout << u << " " << v << " " << w << endl;
            }
        }
    }
    //6    2    3
    for (x = 6; x <= 6; x++) {
        for (y = 2; y <= 2; y++) {
            for (z = 3; z <= 3; z++) {
                cout << x << " " << y << " " << z << endl;
            }
        }
    }
    //7    3    1
    for (a = 7; a <= 7; a++) {
        for (b = 3; b <= 3; b++) {
            for (c = 1; c <= 1; c++) {
                cout << a << " " << b << " " << c << endl;
            }
        }
    } 
    //8    3    2
    for (d = 8; d <= 8; d++) {
        for (e = 3; e <= 3; e++) {
            for (f = 2; f <= 2; f++) {
                cout << d << " " << e << " " << f << endl;
            }
        }
    }
    //9    3    3
    for (g = 9; g <= 9; g++) {
        for (s = 3; s <= 3; s++) {
            for (i2 = 3; i2 <= 3; i2++) {
                cout << g << " " << s << " " << i2 << endl;
            }
        }
    }
}

